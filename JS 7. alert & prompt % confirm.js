// alert - 알려줌
// 메시지를 띄우고 사용자가 확인 버튼을 누르기 전까지는 떠있게 된다.
// 사용자와 상호작용을 한다기 보다는 일방적으로 알리는 용도로 사용한다.
// ex) 비밀번호가 틀렷습니다 or 이름을 입력해 주세요

// prompt - 입력받음
// 어떤 값을 입력 받을때 사용
// const name = prompt("이름을 입력하세요.");
