// const name = prompt("이름을 입력하세요.");
// alert(`안녕하세요. ${name}님 반갑습니다.`);
// prompt는 두개의 파라미터를 받을 수 있다.
// 첫번째는 메시지, 두번째는 디폴트값
// ex) ("예약일을 입력하세요", "2022-10-")
const day = prompt("예약일을 입력하세요", "2022-10-")
alert(`예약일은 ${day}입니다.`);

// confirm - 확인받음
// 확인 받을때 사용
// alert와 다른점은 확인버튼과 취소버튼이 함께 있다.
// 확인을 누르면 true, 취소를 누르면 false
const isAdult = confirm("당신은 성인입니까?");
console.log(isAdult);
// 사용자의 액션을 확인 해 줄때 자주 사용된다.

